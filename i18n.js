export function translatePage() {
    const lang = localStorage.getItem('vibe_lang') || 'en';
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = translations[lang]?.[key];
        if (!text) return;

        // Handle input/textarea placeholders
        if ((el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') && el.hasAttribute('placeholder')) {
            el.placeholder = text;
            return;
        }

        // Handle glitch-text data-text attribute
        if (el.hasAttribute('data-text')) {
            el.setAttribute('data-text', text);
            el.innerText = text;
            return;
        }

        // Handle intro-text data-value attribute
        if (el.hasAttribute('data-value')) {
            el.setAttribute('data-value', text);
            return;
        }

        // Preserve Lucide icon children (both <i data-lucide> and rendered <svg>)
        const icons = Array.from(el.querySelectorAll('i[data-lucide], svg.lucide, svg[class*="lucide"]'));
        el.innerText = text;
        if (icons.length > 0) {
            icons.reverse().forEach(icon => {
                el.prepend(document.createTextNode(' '));
                el.prepend(icon);
            });
        }
    });
}

export function getCurrentLang() {
    return localStorage.getItem('vibe_lang') || 'en';
}

export function setLanguage(lang) {
    localStorage.setItem('vibe_lang', lang);
    translatePage();
}
